    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        boolean validateinput = validateInput();
        
        if (validateinput==false) {
            JOptionPane.showMessageDialog(this, "Username And Password Can Not Be Blank ","Error",JOptionPane.ERROR_MESSAGE);
            return;
        } 
        
        try {
            conn c = new conn();
            String sql = "select * from account where Username='"+jTextField1.getText()+"' and Password = "+jPasswordField1.getText()+"";
            rs = c.s.executeQuery(sql);
            
            int i=0;
            
            while (rs.next()) {                
                i++;
            }
            if (i==1) {
               JOptionPane.showMessageDialog(this, "Login Accepted");
               StartingFrame obj = new StartingFrame();
               obj.setVisible(true);
               this.setVisible(false);
               this.dispose();
               
            } else {
                JOptionPane.showMessageDialog(this, "UserName and Password are not valid","Not Found",JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

